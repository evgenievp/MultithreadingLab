package com.MathWorkderPool;
import java.util.Random;

public class AlgebraicWorker {
    public AlgebraicWorker() {}
    public long factorial(int n) {
        if (n == 1 || n == 0) {
            return 1;
        } else {
            return n * factorial(n - 1);
        }
    }
    public boolean isPrime(int n) {
        if (n <= 1)
            return false;
        for (int i = 2; i < n; i++)
            if (n % i == 0)
                return false;
        return true;
    }

}
